#!/bin/bash

# ============================================================
# Лабораторная работа №11, Задание 3
# Создание и удаление файлов циклом
# ============================================================

# Считываем аргументы
ACTION=$1  # Действие (create или delete)
N=$2       # Количество файлов

# Проверка: переданы ли оба аргумента
if [ -z "$ACTION" ] || [ -z "$N" ]; then
    echo "❌ Ошибка: укажите действие и количество файлов"
    echo "Использование: $0 <create|delete> <N>"
    echo "Пример: $0 create 5"
    exit 1
fi

# Проверка: является ли N числом
if ! [[ "$N" =~ ^[0-9]+$ ]]; then
    echo "❌ Ошибка: N должно быть целым числом"
    exit 1
fi

echo " Начинаю выполнение действия: $ACTION для $N файлов..."

case $ACTION in
    create)
        echo " Режим создания:"
        for ((i=1; i<=N; i++)); do
            touch "${i}.tmp"
            echo "✅ Создан файл: ${i}.tmp"
        done
        ;;
    delete)
        echo "🗑️ Режим удаления:"
        for ((i=1; i<=N; i++)); do
            if [ -f "${i}.tmp" ]; then
                rm "${i}.tmp"
                echo "🗑️ Удален файл: ${i}.tmp"
            else
                echo "️ Файл ${i}.tmp не найден (пропуск)"
            fi
        done
        ;;
    *)
        echo "❌ Неизвестное действие '$ACTION'. Используйте 'create' или 'delete'"
        exit 1
        ;;
esac

echo "✅ Готово!"
