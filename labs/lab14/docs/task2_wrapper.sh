#!/bin/bash

echo "🔨 Компиляция C-программы..."
gcc check_num.c -o check_num

# Проверяем, успешно ли прошла компиляция
if [ $? -ne 0 ]; then
    echo "❌ Ошибка компиляции! Проверьте синтаксис C-кода."
    exit 1
fi

echo "✅ Компиляция успешна."
echo ""
echo "🚀 Запуск C-программы:"
./check_num

# Мгновенно сохраняем код завершения в переменную
STATUS=$?

echo ""
echo "📊 Код завершения программы: $STATUS"
case $STATUS in
    0) echo "🟢 Введённое число равно НУЛЮ" ;;
    1) echo "📈 Введённое число ПОЛОЖИТЕЛЬНОЕ" ;;
    2) echo "📉 Введённое число ОТРИЦАТЕЛЬНОЕ" ;;
    3) echo "⚠️ Ошибка: введено не число!" ;;
    *) echo " Неизвестный код завершения: $STATUS" ;;
esac
