#!/bin/bash

# ============================================================
# Лабораторная работа №11, Задание 1
# Поиск строк в файле с помощью getopts и grep
# ============================================================

# Переменные для хранения параметров
INPUT_FILE=""
OUTPUT_FILE=""
PATTERN=""
CASE_SENSITIVE=""
LINE_NUMBERS=""

# Парсинг командной строки с помощью getopts
# i: -i требует аргумент (имя входного файла)
# o: -o требует аргумент (имя выходного файла)
# p: -p требует аргумент (шаблон поиска)
# C: -C не требует аргумента (регистрозависимый поиск)
# n: -n не требует аргумента (показывать номера строк)

while getopts "i:o:p:Cn" opt; do
    case $opt in
        i)
            INPUT_FILE="$OPTARG"
            echo "📁 Входной файл: $INPUT_FILE"
            ;;
        o)
            OUTPUT_FILE="$OPTARG"
            echo "📄 Выходной файл: $OUTPUT_FILE"
            ;;
        p)
            PATTERN="$OPTARG"
            echo "🔍 Шаблон поиска: $PATTERN"
            ;;
        C)
            CASE_SENSITIVE="-C"
            echo "🔠 Поиск с учётом регистра (ключ -C)"
            ;;
        n)
            LINE_NUMBERS="-n"
            echo "🔢 Показывать номера строк (ключ -n)"
            ;;
        \?)
            echo "❌ Ошибка: неверный ключ -$OPTARG"
            exit 1
            ;;
        :)
            echo "❌ Ошибка: ключ -$OPTARG требует аргумент"
            exit 1
            ;;
    esac
done

# Проверка обязательных параметров
if [ -z "$INPUT_FILE" ] || [ -z "$OUTPUT_FILE" ] || [ -z "$PATTERN" ]; then
    echo ""
    echo "❌ Ошибка: не указаны все обязательные параметры!"
    echo ""
    echo "Использование:"
    echo "  $0 -i <входной_файл> -o <выходной_файл> -p <шаблон> [-C] [-n]"
    echo ""
    echo "Параметры:"
    echo "  -i файл   — файл для поиска"
    echo "  -o файл   — файл для сохранения результатов"
    echo "  -p шаблон — текст для поиска"
    echo "  -C        — учитывать регистр (по умолчанию не учитывается)"
    echo "  -n        — показывать номера строк"
    exit 1
fi

# Проверка существования входного файла
if [ ! -f "$INPUT_FILE" ]; then
    echo "❌ Ошибка: файл '$INPUT_FILE' не найден!"
    exit 1
fi

echo ""
echo "============================================"
echo "🚀 Начинаю поиск..."
echo "============================================"

# Формируем флаги для grep
GREP_FLAGS=""

# Если не указан -C, то делаем поиск без учёта регистра (-i)
if [ -z "$CASE_SENSITIVE" ]; then
    GREP_FLAGS="-i"
fi

# Добавляем флаг номеров строк, если указан
if [ -n "$LINE_NUMBERS" ]; then
    GREP_FLAGS="$GREP_FLAGS -n"
fi

# Выполняем поиск и сохраняем результат
echo "📝 Выполняем: grep $GREP_FLAGS '$PATTERN' '$INPUT_FILE' > '$OUTPUT_FILE'"
grep $GREP_FLAGS "$PATTERN" "$INPUT_FILE" > "$OUTPUT_FILE"

# Проверяем результат
if [ $? -eq 0 ]; then
    echo "✅ Поиск завершён успешно!"
    echo ""
    
    # Считаем количество найденных строк
    COUNT=$(wc -l < "$OUTPUT_FILE")
    echo "📊 Найдено строк: $COUNT"
    echo ""
    
    # Показываем результат
    if [ "$COUNT" -gt 0 ]; then
        echo "📋 Результаты поиска:"
        echo "----------------------------------------"
        cat "$OUTPUT_FILE"
        echo "----------------------------------------"
    fi
else
    echo "⚠️  Ничего не найдено или произошла ошибка"
    exit 1
fi
