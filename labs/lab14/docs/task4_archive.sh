#!/bin/bash

# ============================================================
# Лабораторная работа №11, Задание 4
# Архивация файлов, изменённых менее недели назад
# ============================================================

# Проверяем, передан ли путь к директории
if [ -z "$1" ]; then
    echo "❌ Ошибка: укажите путь к директории"
    echo "Использование: $0 <путь_к_папке>"
    exit 1
fi

TARGET_DIR="$1"

# Проверяем существование директории
if [ ! -d "$TARGET_DIR" ]; then
    echo "❌ Ошибка: директория '$TARGET_DIR' не найдена!"
    exit 1
fi

ARCHIVE_NAME="week_backup.tar.gz"

echo "🔍 Ищем файлы в '$TARGET_DIR', изменённые менее 7 дней назад..."

# find флаги:
# -maxdepth 1 : искать только в текущей папке (не в подпапках)
# -type f     : только файлы (не папки)
# -mtime -7   : время изменения < 7 суток
FILE_LIST=$(find "$TARGET_DIR" -maxdepth 1 -type f -mtime -7)

# Если ничего не найдено
if [ -z "$FILE_LIST" ]; then
    echo "⚠️  Файлов за последнюю неделю не найдено."
    exit 0
fi

echo "📋 Найденные файлы:"
echo "$FILE_LIST"
echo ""

# Создаём архив
# tar -czf : создать gz-архив
# $FILE_LIST : список найденных файлов
tar -czf "$ARCHIVE_NAME" $FILE_LIST

# Проверяем успех
if [ $? -eq 0 ]; then
    echo "✅ Архив успешно создан: $ARCHIVE_NAME"
    echo "📊 Размер архива:"
    ls -lh "$ARCHIVE_NAME"
else
    echo "❌ Ошибка при создании архива!"
    exit 1
fi
