#!/bin/bash

# ============================================================
# Лабораторная работа №12, Задание 4
# Подсчет файлов по расширению в указанной директории
# ============================================================

# Проверка количества аргументов (нужно 2: расширение и путь)
if [ $# -lt 2 ]; then
    echo "Ошибка: Недостаточно аргументов."
    echo "Использование: $0 <расширение> <путь_к_директории>"
    echo "Пример: $0 .sh /home/user/projects"
    exit 1
fi

EXTENSION="$1"
TARGET_DIR="$2"

# Проверка, существует ли указанная директория
if [ ! -d "$TARGET_DIR" ]; then
    echo "Ошибка: Директория '$TARGET_DIR' не найдена."
    exit 1
fi

echo "Поиск файлов с расширением '$EXTENSION' в директории: $TARGET_DIR"
echo "------------------------------------------------------------"

COUNT=0

# Перебираем все файлы, совпадающие с шаблоном *расширение
# Используем кавычки, чтобы пути с пробелами обрабатывались верно
for file in "$TARGET_DIR"/*"$EXTENSION"; do
    
    # Проверка: существует ли файл (на случай если маска ничего не нашла)
    # и является ли он обычным файлом (а не папкой)
    if [ -f "$file" ]; then
        COUNT=$((COUNT + 1))
        # Можно раскомментировать строку ниже, чтобы видеть список найденных файлов
        # echo "Найден: $(basename "$file")"
    fi
done

echo "------------------------------------------------------------"
echo "Всего найдено файлов: $COUNT"
