#!/bin/bash

# ============================================================
# Лабораторная работа №12, Задание 3
# Аналог команды ls без использования ls/dir
# ============================================================

# Если аргумент не передан, используем текущую директорию
if [ $# -eq 0 ]; then
    TARGET_DIR="."
else
    TARGET_DIR="$1"
fi

# Проверяем, существует ли указанная директория
if [ ! -d "$TARGET_DIR" ]; then
    echo "Ошибка: '$TARGET_DIR' не является директорией или не существует!"
    exit 1
fi

echo "Содержимое директории: $TARGET_DIR"
echo "------------------------------------------------------------"
# Форматируем вывод как таблицу
printf "%-30s %-5s %-10s\n" "Имя файла" "Тип" "Права (R/W/X)"
echo "------------------------------------------------------------"

# Перебираем все файлы в директории
# Используем звездочку (*) для получения списка
for file in "$TARGET_DIR"/*; do
    
    # Проверяем, существует ли файл (на случай если директория пуста)
    [ -e "$file" ] || continue

    # Получаем имя файла без пути
    filename=$(basename "$file")

    # Определяем тип файла
    type_str="file"
    [ -d "$file" ] && type_str="dir"
    [ -L "$file" ] && type_str="link" # Символическая ссылка

    # Проверяем права доступа
    # Используем операторы проверки из раздела 10.2.8 (-r, -w, -x)
    
    # Чтение (Read)
    if [ -r "$file" ]; then r="r"; else r="-"; fi
    
    # Запись (Write)
    if [ -w "$file" ]; then w="w"; else w="-"; fi
    
    # Исполнение (Execute)
    if [ -x "$file" ]; then x="x"; else x="-"; fi

    perms="${r}${w}${x}"

    # Выводим информацию
    printf "%-30s %-5s %-10s\n" "$filename" "$type_str" "$perms"
done
