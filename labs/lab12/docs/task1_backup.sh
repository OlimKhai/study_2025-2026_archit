#!/bin/bash

# ============================================================
# Лабораторная работа №12, Задание 1
# Скрипт резервного копирования самого себя
# ============================================================

# Создаём директорию backup в домашнем каталоге (если не существует)
mkdir -p ~/backup

# Получаем имя текущего скрипта
SCRIPT_NAME=$(basename "$0")

# Получаем полный путь к скрипту
SCRIPT_PATH="$(readlink -f "$0")"

# Создаём временную метку для уникальности имени архива
TIMESTAMP=$(date +%Y%m%d_%H%M%S)

# Имя архива с временной меткой
BACKUP_NAME="${SCRIPT_NAME}.${TIMESTAMP}.tar.gz"

# Полный путь к архиву
BACKUP_PATH="~/backup/${BACKUP_NAME}"

echo "============================================"
echo "Резервное копирование скрипта: $SCRIPT_NAME"
echo "============================================"
echo "Исходный файл: $SCRIPT_PATH"
echo "Архив: $BACKUP_PATH"
echo ""

# Создаём архив с помощью tar
# tar -czf = tar с сжатием gzip
tar -czf ~/backup/${BACKUP_NAME} -C "$(dirname "$SCRIPT_PATH")" "$(basename "$SCRIPT_PATH")"

# Проверяем, успешно ли создался архив
if [ $? -eq 0 ]; then
    echo "✓ Резервная копия успешно создана!"
    echo "✓ Размер архива: $(du -h ~/backup/${BACKUP_NAME} | cut -f1)"
    echo ""
    echo "Список файлов в backup:"
    ls -lh ~/backup/
else
    echo "✗ Ошибка при создании резервной копии!"
    exit 1
fi

