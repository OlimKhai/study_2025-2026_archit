#!/bin/bash

# ============================================================
# Лабораторная работа №12, Задание 2
# Обработка произвольного числа аргументов (в т.ч. > 10)
# ============================================================

echo "=================================================="
echo "Всего передано аргументов: $#"
echo "=================================================="

# Способ 1: Цикл for (самый надежный способ перебрать ВСЕ аргументы)
echo "Список всех аргументов:"
count=1
for arg in "$@"
do
    echo "  Аргумент №$count: $arg"
    count=$((count + 1))
done

echo ""
echo "=================================================="

# Способ 2: Явный доступ к аргументам > 9
# В Bash к аргументам начиная с 10-го нужно обращаться через ${10}, ${11} и т.д.
if [ $# -ge 10 ]; then
    echo "Проверка доступа к аргументам > 9:"
    echo "  10-й аргумент: ${10}"
    if [ $# -ge 11 ]; then
        echo "  11-й аргумент: ${11}"
    fi
fi
